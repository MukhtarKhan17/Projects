/**
*==============================================================================
*                         \V/ Function Information
*------------------------------------------------------------------------------
* @fn  void UniqueId_Read (uint8_t uid_buf[])
*==============================================================================
* @brief    UniqueId_Read
*           ---
*           ---
*           ---
* @note     None
* @param    uid_buf[]
* @retval   None
* @author   MK
* @date     18-OCT-2024 // >> Enter Last Change Date
* @remark   Designed & Developed by Voidron Co.
*==============================================================================
*/
void UniqueId_Read (uint8_t uid_buf[])
{
	#define UID_PART1_ADDR             (UNIQUE_ID_BASE_ADDR + 0U)
	#define UID_PART2_ADDR             (UNIQUE_ID_BASE_ADDR + 4U)
	#define UID_PART3_ADDR             (UNIQUE_ID_BASE_ADDR + 8U)

	uint32_t uid_part1 = *(uint32_t *)(UID_PART1_ADDR);  // First 32 bits (LSB)
	uint32_t uid_part2 = *(uint32_t *)(UID_PART2_ADDR);  // Middle 32 bits
	uint32_t uid_part3 = *(uint32_t *)(UID_PART3_ADDR);  // Last 32 bits (MSB)

	uid_buf[0]  = (uint8_t)((uid_part1 >> 24) & 0xFF);
	uid_buf[1]  = (uint8_t)((uid_part1 >> 16) & 0xFF);
	uid_buf[2]  = (uint8_t)((uid_part1 >> 8)  & 0xFF);
	uid_buf[3]  = (uint8_t)((uid_part1 >> 0)  & 0xFF);

	uid_buf[4]  = (uint8_t)((uid_part2 >> 24) & 0xFF);
	uid_buf[5]  = (uint8_t)((uid_part2 >> 16) & 0xFF);
	uid_buf[6]  = (uint8_t)((uid_part2 >> 8)  & 0xFF);
	uid_buf[7]  = (uint8_t)((uid_part2 >> 0)  & 0xFF);

	uid_buf[8]  = (uint8_t)((uid_part3 >> 24) & 0xFF);
	uid_buf[9]  = (uint8_t)((uid_part3 >> 16) & 0xFF);
	uid_buf[10] = (uint8_t)((uid_part3 >> 8)  & 0xFF);
	uid_buf[11] = (uint8_t)((uid_part3 >> 0)  & 0xFF);
}// >> End of UniqueId_Read

