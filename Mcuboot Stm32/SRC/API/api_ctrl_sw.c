/**
*==============================================================================
*                         \V/ Function Information
*------------------------------------------------------------------------------
* @fn  void CtrlSw_Init (void)
*==============================================================================
* @brief    CtrlSw_Init
*           ---
*           ---
*           ---
* @note     None
* @param    None
* @retval   None
* @author   MK
* @date     21-FEB-2021 // >>Enter Last Change Date
* @remark   Designed & Developed by Voidron Co.
*==============================================================================
*/
void CtrlSw_Init (void)
{
	GPIO_InitTypeDef  GPIO_InitStruct;

	RCC_APB2PeriphClockCmd(CTRL_SW_PORT_CLK_EN, ENABLE);

	GPIO_InitStruct.GPIO_Pin = CTRL_SW_PIN_DEF ;
	GPIO_InitStruct.GPIO_Mode = GPIO_Mode_IN_FLOATING;
	GPIO_Init(CTRL_SW_PORT_DEF, &GPIO_InitStruct);
} // End of CtrlSw_Init()

/**
*==============================================================================
*                         \V/ Function Information
*------------------------------------------------------------------------------
* @fn  bool_t CtrlSw_GetStatus (void)
*==============================================================================
* @brief    CtrlSw_GetStatus
*           ---
*           ---
*           ---
* @note     None
* @param    None
* @retval   sense_sts_f
* @author   MK
* @date     21-FEB-2021 // >>Enter Last Change Date
* @remark   Designed & Developed by Voidron Co.
*==============================================================================
*/
bool_t CtrlSw_GetSts (void)
{
	bool_t sw_sts_f = FALSE;

	if(GPIO_ReadInputDataBit(CTRL_SW_PORT_DEF, CTRL_SW_PIN_DEF) == Bit_RESET)
	{
		sw_sts_f = TRUE;
	}

	return(sw_sts_f);

}// >>End of CtrlSw_GetSts

