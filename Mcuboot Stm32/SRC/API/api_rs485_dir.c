/**
*==============================================================================
*                         \V/ Function Information
*------------------------------------------------------------------------------
* @fn  void Rs485Dir_Init (void)
*==============================================================================
* @brief    Rs485Dir_Init
*           ---
*           ---
*           ---
* @note     None
* @param    None
* @retval   None
* @author   MK
* @date     22-AUG-2021 // >>Enter Last Change Date
* @remark   Designed & Developed by Voidron Co.
*==============================================================================
*/
void Rs485Dir_Init (void)
{
	GPIO_InitTypeDef  GPIO_InitStruct;

	RCC_APB2PeriphClockCmd(RS485_DIR_PORT_CLK_EN, ENABLE);

	GPIO_InitStruct.GPIO_Pin = RS485_DIR_PIN_DEF ;
	GPIO_InitStruct.GPIO_Mode = GPIO_Mode_Out_PP;
	GPIO_InitStruct.GPIO_Speed = GPIO_Speed_50MHz;
	GPIO_Init(RS485_DIR_PORT_DEF, &GPIO_InitStruct);

	Rs485Dir_RxOn();
} // End of Rs485Dir_Init()

/**
*==============================================================================
*                         \V/ Function Information
*------------------------------------------------------------------------------
* @fn  void Rs485Dir_TxOn (void)
*==============================================================================
* @brief    Rs485Dir_TxOn
*           ---
*           ---
*           ---
* @note     None
* @param    None
* @retval   None
* @author   MK
* @date     22-AUG-2021 // >>Enter Last Change Date
* @remark   Designed & Developed by Voidron Co.
*==============================================================================
*/
void Rs485Dir_TxOn (void)
{
	GPIO_SetBits(RS485_DIR_PORT_DEF, RS485_DIR_PIN_DEF);
} // End of Rs485Dir_TxOn()

/**
*==============================================================================
*                         \V/ Function Information
*------------------------------------------------------------------------------
* @fn  void Rs485Dir_RxOn (void)
*==============================================================================
* @brief    Rs485Dir_RxOn
*           ---
*           ---
*           ---
* @note     None
* @param    None
* @retval   None
* @author   MK
* @date     22-AUG-2021 // >>Enter Last Change Date
* @remark   Designed & Developed by Voidron Co.
*==============================================================================
*/
void Rs485Dir_RxOn (void)
{
	GPIO_ResetBits(RS485_DIR_PORT_DEF, RS485_DIR_PIN_DEF);
} // End of Rs485Dir_RxOn()

