void Keypad_Init (void)
{
	keypad_base_t set_keypad_base_s = {0};

	set_keypad_base_s.GpioInit   = &KeypadIO_GpioInit;
	set_keypad_base_s.RdKeySts   = &KeypadIO_RdKeySts;
	set_keypad_base_s.GetTickMs  = &SysTick_GetTickTmrVal;
	set_keypad_base_s.ChkTimeout = &SysTick_CheckTimeOut;

	set_keypad_base_s.num_of_keys           = KEYPAD_KEY_COUNT;
	set_keypad_base_s.debounce_timeout_ms   = KEYPAD_DEBOUNCE_MS;
	set_keypad_base_s.key_press_timeout_ms  = KEYPAD_KEY_PRESS_MS;
	set_keypad_base_s.key_detect_method     = KEY_DETECT_ON_RELEASE;

	Keypad_BaseInit(set_keypad_base_s);
	(void)Keypad_Enable(vTag_Keypad_Key);
} // End of Keypad_Init()


