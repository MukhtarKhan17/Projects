/**
*==============================================================================
*                         \V/ Function Information
*------------------------------------------------------------------------------
* @fn  void Watchdog_Init (void)
*==============================================================================
* @brief    Watchdog_Init for 3.2sec
*           ---
*           ---
*           ---
* @note     None
* @param    None
* @retval   None
* @author   MK
* @date     02-MAR-2021 // >> Enter Last Change Date
* @remark   Designed & Developed by Voidron Co.
*==============================================================================
*/
void Watchdog_Init (void)
{
	  /* Enable the LSI OSC */
	  RCC_LSICmd(ENABLE);
	  /* Wait till LSI is ready */
	  while (RCC_GetFlagStatus(RCC_FLAG_LSIRDY) == RESET);

	  /* Enable Watchdog*/
	  IWDG_WriteAccessCmd(IWDG_WriteAccess_Enable);
	  IWDG_SetPrescaler(IWDG_Prescaler_32); // 4, 8, 16 ... 256
	  IWDG_SetReload(0x0FFF);//This parameter must be a number between 0 and 0x0FFF.
	  IWDG_ReloadCounter();
	  IWDG_Enable();
}// >> End of Watchdog_Init

