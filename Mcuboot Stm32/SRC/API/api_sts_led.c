void StsLed_TurnOff (void)
{
	StsLedIO_SetHigh();
} // End of StsLed_TurnOff()

/**
*==============================================================================
*                         \V/ Function Information
*------------------------------------------------------------------------------
* @fn  void StsLed_Toggle (void)
*==============================================================================
* @brief    StsLed_Toggle
*           ---
*           ---
*           ---
* @note     None
* @param    None
* @retval   None
* @author   MK
* @date     21-FEB-2021 // >> Enter Last Change Date
* @remark   Designed & Developed by Voidron Co.
*==============================================================================
*/
void StsLed_Toggle (void)
{
	static bool_t toggle_f = FALSE;

	if(toggle_f == TRUE)
	{
		StsLed_TurnOn();
		toggle_f = FALSE;
	}
	else
	{
		StsLed_TurnOff();
		toggle_f = TRUE;
	}
} // End of StsLed_Toggle()

