void UartCom3_Init (void)
{
	uart_com3_base_t set_uart_com3_base_s = {0};

	Usart3_Init(&UartCom3_TxIsrPrcs, &UartCom3_RxIsrPrcs);

	set_uart_com3_base_s.UartTx = &Usart3_TxData;
	set_uart_com3_base_s.UartRx = &Usart3_RxData;
	set_uart_com3_base_s.rx_timeout_ms = 25;
	UartCom3_BaseInit(set_uart_com3_base_s);

	(void)UartCom3_Enable(vTag_UartCom3_Key);
} // End of UartCom3_Init()

