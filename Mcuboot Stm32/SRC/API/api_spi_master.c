/**
*==============================================================================
*                         \V/ Function Information
*------------------------------------------------------------------------------
* @fn  void SpiMaster_TxData (void *ip_data_ptr, uint16_t data_sz)
*==============================================================================
* @brief    SpiMaster_TxData
*           ---
*           ---
*           ---
* @note     None
* @param    *ip_data_ptr
* @param    data_sz
* @retval   None
* @author   MK
* @date     16-JAN-2022 // >> Enter Last Change Date
* @remark   Designed & Developed by Voidron Co.
*==============================================================================
*/
void SpiMaster_TxData (void *ip_data_ptr, uint16_t data_sz)
{
	uint8_t *data_ptr = ip_data_ptr;
	uint8_t tx_byte = 0;
	uint16_t i = 0;

	Spi2_MasterSetCsLow();

	for(i = 0; i < data_sz; i++)
	{
		tx_byte = *data_ptr;
		(void)Spi2_MasterTxRxByte(tx_byte);
		data_ptr++;
	}

	Spi2_MasterSetCsHigh();

}// End of SpiMaster_TxData()

/**
*==============================================================================
*                         \V/ Function Information
*------------------------------------------------------------------------------
* @fn  void SpiMaster_RxData (void *op_data_ptr, uint16_t data_sz)
*==============================================================================
* @brief    SpiMaster_RxData
*           ---
*           ---
*           ---
* @note     None
* @param    *op_data_ptr
* @param    data_sz
* @retval   None
* @author   MK
* @date     16-JAN-2022 // >> Enter Last Change Date
* @remark   Designed & Developed by Voidron Co.
*==============================================================================
*/
void SpiMaster_RxData (void *op_data_ptr, uint16_t data_sz)
{
	uint8_t *data_ptr = op_data_ptr;
	uint8_t rx_byte = 0;
	uint16_t i = 0;

	Spi2_MasterSetCsLow();

	for(i = 0; i < data_sz; i++)
	{
		rx_byte = Spi2_MasterTxRxByte(0);
		*data_ptr = rx_byte;
		data_ptr++;
	}

	Spi2_MasterSetCsHigh();
}// End of SpiMaster_RxData()


