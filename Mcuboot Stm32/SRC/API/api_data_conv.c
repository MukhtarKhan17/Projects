/**
*==============================================================================
*                         \V/ Function Information
*------------------------------------------------------------------------------
* @fn  uint8_t DataConv_NibbleToAscii (uint8_t nibble)
*==============================================================================
* @brief    DataConv_NibbleToAscii
*           ---
*           ---
*           ---
* @note     nibble range 0 to 15 (4-bit)
* @param    nibble
* @retval   ascii_val
* @author   MK
* @date     16-OCT-2024 // >> Enter Last Change Date
* @remark   Designed & Developed by Voidron Co.
*==============================================================================
*/
uint8_t DataConv_NibbleToAscii (uint8_t nibble)
{
	#define NIBBLE_MASK                (0x0FU)

	uint8_t ascii_val = 0;

	nibble = (nibble & NIBBLE_MASK);

	if(nibble < 10)
	{
		ascii_val = (nibble + '0');
	}
	else
	{
		ascii_val = (nibble - 10 + 'A');
	}

	return(ascii_val);
} // End of DataConv_NibbleToAscii()

