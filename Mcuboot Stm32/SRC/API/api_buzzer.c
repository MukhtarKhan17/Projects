void Buzzer_Disable (void)
{
	buzzer_enable_f = FALSE;
}//End of Buzzer_Disable

/**
 *==============================================================================
 *                         \V/ Function Information
 *------------------------------------------------------------------------------
 * @fn  void Buzzer_Beep (uint16_t set_beep_ms)
 *==============================================================================
 * @brief    Buzzer_Beep
 *           ---
 *           ---
 *           ---
 * @note     None
 * @param    set_beep_ms
 * @retval   None
 * @author   RS
 * @date     17-OCT-2022
 * @remark   Designed & Developed by Voidron Co.
 *==============================================================================
 */
void Buzzer_Beep (uint16_t set_beep_ms)
{
	if(buzzer_enable_f == TRUE)
	{
		buzzer_beep_tmr = set_beep_ms;
		BuzzIO_TurnOn();
	}
}//End of Buzzer_Beep

/**
 *==============================================================================
 *                         \V/ Function Information
 *------------------------------------------------------------------------------
 * @fn  void Buzzer_TickIsrPrcs (void)
 *==============================================================================
 * @brief    Buzzer_TickIsrPrcs
 *           ---
 *           ---
 *           ---
 * @note     None
 * @param    None
 * @retval   None
 * @author   RS
 * @date     17-OCT-2022
 * @remark   Designed & Developed by Voidron Co.
 *==============================================================================
 */
void Buzzer_TickIsrPrcs (void)
{
	if(buzzer_beep_tmr == 0)
	{
		BuzzIO_TurnOff();
	}
	else
	{
		buzzer_beep_tmr--;
	}
}//End of Buzzer_TickIsrPrcs


