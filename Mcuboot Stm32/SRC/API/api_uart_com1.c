void UartCom1_Init (void)
{
	uart_com1_base_t set_uart_com1_base_s = {0};

	Usart1_Init(&UartCom1_TxIsrPrcs, &UartCom1_RxIsrPrcs);

	set_uart_com1_base_s.UartTx = &Usart1_TxData;
	set_uart_com1_base_s.UartRx = &Usart1_RxData;
	set_uart_com1_base_s.rx_timeout_ms = 25;
	UartCom1_BaseInit(set_uart_com1_base_s);

	(void)UartCom1_Enable(vTag_UartCom1_Key);

} // End of UartCom1_Init()

