/**
 *==============================================================================
 *                         \V/ Function Information
 *------------------------------------------------------------------------------
 * @fn  void BuzzIO_GpioInit (void)
 *==============================================================================
 * @brief    Init
 *           ---
 *           ---
 *           ---
 * @note     None
 * @param    None
 * @retval   None
 * @author   RS
 * @date     08-JAN-2023
 * @remark   Designed & Developed by Voidron Co.
 *==============================================================================
 */
void BuzzIO_GpioInit  (void)
{
	GPIO_InitTypeDef  GPIO_InitStructure;

	RCC_APB2PeriphClockCmd(BUZZ_IO_PORT_CLK, ENABLE);

	GPIO_InitStructure.GPIO_Pin   = BUZZ_IO_PIN;
	GPIO_InitStructure.GPIO_Mode  = GPIO_Mode_Out_PP;
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
	GPIO_Init(BUZZ_IO_PORT , &GPIO_InitStructure);

}// >> End of BuzzIO_GpioInit

/**
 *==============================================================================
 *                         \V/ Function Information
 *------------------------------------------------------------------------------
 * @fn  void BuzzIO_TurnOn (void)
 *==============================================================================
 * @brief    Set On
 *           ---
 *           ---
 *           ---
 * @note     None
 * @param    None
 * @retval   None
 * @author   RS
 * @date     08-JAN-2023
 * @remark   Designed & Developed by Voidron Co.
 *==============================================================================
 */
void BuzzIO_TurnOn (void)
{
	GPIO_SetBits(BUZZ_IO_PORT, BUZZ_IO_PIN);
}// >> End of BuzzIO_TurnOn

/**
 *==============================================================================
 *                         \V/ Function Information
 *------------------------------------------------------------------------------
 * @fn  void BuzzIO_TurnOff (void)
 *==============================================================================
 * @brief    Set Off
 *           ---
 *           ---
 *           ---
 * @note     None
 * @param    None
 * @retval   None
 * @author   RS
 * @date     08-JAN-2023
 * @remark   Designed & Developed by Voidron Co.
 *==============================================================================
 */
void BuzzIO_TurnOff (void)
{
	GPIO_ResetBits(BUZZ_IO_PORT, BUZZ_IO_PIN);
}// >> End of BuzzIO_TurnOff


/**
 *==============================================================================
 *                         \V/ Function Information
 *------------------------------------------------------------------------------
 * @fn  void BuzzIO_Toggle (void)
 *==============================================================================
 * @brief    Set Off
 *           ---
 *           ---
 *           ---
 * @note     None
 * @param    None
 * @retval   None
 * @author   RS
 * @date     08-JAN-2023
 * @remark   Designed & Developed by Voidron Co.
 *==============================================================================
 */
void BuzzIO_Toggle(void)
{
	static bool_t toggle_f = FALSE;

	if(toggle_f == TRUE)
	{
		BuzzIO_TurnOn();
		toggle_f = FALSE;
	}
	else
	{
		BuzzIO_TurnOff();
		toggle_f = TRUE;
	}
}
